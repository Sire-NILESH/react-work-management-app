import { Menu, Transition } from "@headlessui/react";
import { Fragment } from "react";
import { allTasksActions } from "../store/all-tasks-slice";
import { useSelector, useDispatch } from "react-redux";

export default function CardsMore(props) {
  const currOption = useSelector((state) => state.currentOption);
  const dispatch = useDispatch();

  const showCardFormHandler = () => {
    return props.openModal();
  };

  const moveCardToDoneHandler = () => {
    dispatch(
      allTasksActions.moveCard({
        taskType: currOption.taskType,
        taskId: currOption.taskId,
        cardData: {
          cardId: props.cardId,
          currentCategory: props.cardCategory,
          targetCategory: "doneCards",
        },
      })
    );
  };

  const deleteCardHandler = () => {
    dispatch(
      allTasksActions.deleteCard({
        taskType: currOption.taskType,
        taskId: currOption.taskId,
        cardData: { cardId: props.cardId, category: props.cardCategory },
      })
    );
  };

  return (
    <div className=" w-56 text-right">
      <Menu as="div" className=" relative inline-block text-left">
        <div>
          <Menu.Button>{props.children}</Menu.Button>
        </div>
        <Transition
          as={Fragment}
          enter="transition ease-out duration-100"
          enterFrom="transform opacity-0 scale-95"
          enterTo="transform opacity-100 scale-100"
          leave="transition ease-in duration-75"
          leaveFrom="transform opacity-100 scale-100"
          leaveTo="transform opacity-0 scale-95"
        >
          <Menu.Items className="card-todo absolute right-0 z-20 mt-2 w-64 origin-top-right divide-y divide-gray-200 rounded-md !bg-[#e9f6ff] px-4 py-2 !shadow-xl  ring-opacity-5 focus:outline-none dark:divide-gray-600  dark:!bg-gray-700 dark:shadow-none md:w-44  md:px-0 ">
            <div className="px-1 py-1 ">
              <Menu.Item>
                {({ active }) => (
                  <button
                    className={`${
                      active
                        ? "bg-blue-700 text-white"
                        : "text-gray-900 dark:!text-slate-300"
                    } group flex w-full items-center rounded-md px-2 py-2 text-sm`}
                    onClick={showCardFormHandler}
                  >
                    {active ? (
                      <EditActiveIcon
                        className="mr-2 h-5 w-5"
                        aria-hidden="true"
                      />
                    ) : (
                      <EditInactiveIcon
                        className="mr-2 h-5 w-5"
                        aria-hidden="true"
                      />
                    )}
                    Edit
                  </button>
                )}
              </Menu.Item>
            </div>
            <div className="px-1 py-1">
              <Menu.Item>
                {({ active }) => (
                  <button
                    className={`${
                      active ? "bg-blue-700 text-white" : "text-gray-900"
                    } group flex w-full items-center rounded-md px-2 py-2 text-sm dark:!text-slate-300`}
                    onClick={moveCardToDoneHandler}
                  >
                    {active ? (
                      <MoveActiveIcon
                        className="mr-2 h-5 w-5"
                        aria-hidden="true"
                      />
                    ) : (
                      <MoveInactiveIcon
                        className="mr-2 h-5 w-5"
                        aria-hidden="true"
                      />
                    )}
                    Move to DONE
                  </button>
                )}
              </Menu.Item>
            </div>
            <div className="px-1 py-1">
              <Menu.Item>
                {({ active }) => (
                  <button
                    className={`${
                      active ? "bg-blue-700 text-white" : "text-gray-900"
                    } group flex w-full items-center rounded-md px-2 py-2 text-sm dark:!text-slate-300`}
                    onClick={deleteCardHandler}
                  >
                    {active ? (
                      <DeleteActiveIcon
                        className="mr-2 h-5 w-5 text-violet-400"
                        aria-hidden="true"
                      />
                    ) : (
                      <DeleteInactiveIcon
                        className="mr-2 h-5 w-5 text-violet-400"
                        aria-hidden="true"
                      />
                    )}
                    Delete
                  </button>
                )}
              </Menu.Item>
            </div>
          </Menu.Items>
        </Transition>
      </Menu>
    </div>
  );
}

function EditInactiveIcon(props) {
  return (
    <svg
      {...props}
      viewBox="0 0 20 20"
      fill="none"
      xmlns="http://www.w3.org/2000/svg"
    >
      <path
        d="M4 13V16H7L16 7L13 4L4 13Z"
        fill="#EDE9FE"
        stroke="#A78BFA"
        strokeWidth="2"
      />
    </svg>
  );
}

